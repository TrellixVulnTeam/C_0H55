#include <stdio.h>

int main() {
  
   printf("The total of 6 and 15 is %d\n", 6 + 15);
   printf("The sum of %f and %f is %f\n", 12.2, 15.754, 12.2 + 15.754);

   printf("%f plus %f equals %f\n", 15.0, 2.0, 15.0 + 2.0);
   printf("%f minus %f equals %f\n", 15.0, 2.0, 15.0 - 2.0);
   printf("%f times %f equals %f\n", 15.0, 2.0, 15.0 * 2.0);
   printf("%f divided %f equals %f\n", 15.0, 2.0, 15.0 / 2.0);

   return 0;

} 
