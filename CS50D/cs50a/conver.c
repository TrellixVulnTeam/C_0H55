#include <stdio.h>

int main () {
   
   printf("The value of the decimal number 9 in octal is %o.\n", 9);
   printf("The value of the decimal number 9 in hexadecimal is %x.\n", 9);
   printf("The value of the decimal number 14 in octal is %o.\n", 14);
   printf("The value of the decimal number 14 in hexadecimal is %x.\n", 14);


   return 0;

}
